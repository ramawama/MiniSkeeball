// Scale 1: 6x8px per char (fits directly in one SSD1306 page)
static void draw_char_1x(uint8_t page, uint8_t col, char c) {
    if (c < 0x20 || c > 0x7E) c = '?';
    const uint8_t *g = font5x7[(uint8_t)(c - 0x20)];
    uint8_t pixels[6] = {g[0], g[1], g[2], g[3], g[4], 0x00};
    set_cursor(page, col);
    send_data(pixels, 6);
}


void display_init(void) {
    printf("display: init\n");

    nrf_delay_ms(50);

    nrfx_twim_config_t cfg = NRFX_TWIM_DEFAULT_CONFIG;
    cfg.scl = I2C_QWIIC_SCL;
    cfg.sda = I2C_QWIIC_SDA;

    nrfx_err_t err = nrfx_twim_init(&twim, &cfg, NULL, NULL);
    if (err == NRFX_SUCCESS) {
        nrfx_twim_enable(&twim);
    }

    nrf_delay_ms(10);
    hw_init();
    display_clear();
    printf("display: ready\n");
}

void display_clear(void) {
    static const uint8_t zeros[OLED_WIDTH] = {0};
    for (uint8_t page = 0; page < OLED_PAGES; page++) {
        set_cursor(page, 0);
        send_data(zeros, OLED_WIDTH);
    }
}

void display_show_text(const char *s) {
    display_clear();

    // Count lines for vertical centering
    uint8_t num_lines = 1;
    for (const char *p = s; *p; p++) {
        if (*p == '\n') num_lines++;
    }
    uint8_t page = (OLED_PAGES - num_lines) / 2;

    while (*s && page < OLED_PAGES) {
        uint8_t len = 0;
        const char *p = s;
        while (*p && *p != '\n') { len++; p++; }

        uint8_t col = (OLED_WIDTH - len * 6) / 2;
        for (uint8_t i = 0; i < len; i++) {
            draw_char_1x(page, col + i * 6, s[i]);
        }
        s = (*p == '\n') ? p + 1 : p;
        page++;
    }
}

void display_flash(void) {
    send_cmd(0xA7);  // invert display
    nrf_delay_ms(120);
    send_cmd(0xA6);  // normal display
}
