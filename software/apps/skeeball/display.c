#define OLED_ADDR   0x3D
#define OLED_WIDTH  128
#define OLED_PAGES  8      // 64px / 8 = 8 pages

static const nrfx_twim_t twim = NRFX_TWIM_INSTANCE(0);

static uint8_t cmd_buf[2];
static uint8_t data_buf[OLED_WIDTH + 1];  // control byte + up to 128 data bytes

// ---------------------------------------------------------------------------
// I2C helpers
// ---------------------------------------------------------------------------

static void send_cmd(uint8_t cmd) {
    cmd_buf[0] = 0x00;  // control byte: command stream
    cmd_buf[1] = cmd;
    nrfx_twim_xfer_desc_t xfer = NRFX_TWIM_XFER_DESC_TX(OLED_ADDR, cmd_buf, 2);
    nrfx_twim_xfer(&twim, &xfer, 0);
}

static void send_data(const uint8_t *data, uint8_t len) {
    data_buf[0] = 0x40;  // control byte: data stream
    for (uint8_t i = 0; i < len; i++) data_buf[i + 1] = data[i];
    nrfx_twim_xfer_desc_t xfer = NRFX_TWIM_XFER_DESC_TX(OLED_ADDR, data_buf, len + 1);
    nrfx_twim_xfer(&twim, &xfer, 0);
}

// Set cursor position in page addressing mode
static void set_cursor(uint8_t page, uint8_t col) {
    send_cmd(0xB0 | page);
    send_cmd(0x00 | (col & 0x0F));
    send_cmd(0x10 | (col >> 4));
}

// ---------------------------------------------------------------------------
// SSD1306 init sequence
// ---------------------------------------------------------------------------
static void hw_init(void) {
    static const uint8_t cmds[] = {
        0xAE,        // display off
        0xD5, 0x80,  // clock divider
        0xA8, 0x3F,  // multiplex 1/64
        0xD3, 0x00,  // display offset 0
        0x40,        // start line 0
        0x8D, 0x14,  // charge pump on
        0x20, 0x02,  // page addressing mode
        0xA1,        // seg remap
        0xC8,        // COM scan reversed
        0xDA, 0x12,  // COM pins config
        0x81, 0xCF,  // contrast
        0xD9, 0xF1,  // precharge
        0xDB, 0x40,  // VCOMH
        0xA4,        // display from RAM
        0xA6,        // normal (not inverted)
        0xAF,        // display on
    };
    for (uint8_t i = 0; i < sizeof(cmds); i++) send_cmd(cmds[i]);
}

